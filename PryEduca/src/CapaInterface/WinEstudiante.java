    private void BtnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCrearActionPerformed
        int k=WinEduca.lst.numeroest;
        cedula=TxtCedula.getText();
        nombre=TxtNombre.getText();
        WinEduca.lst._lstest[k]=new ClsEstudiante(cedula,nombre);
        k+=1;
        WinEduca.lst.numeroest=k;
        TxtNombre.setText(null);
        TxtCedula.setText(null);
        TxtCedula.requestFocus();
    }//GEN-LAST:event_BtnCrearActionPerformed

    private void BtnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnListarActionPerformed
        CmbLista.removeAllItems();
        String linea;
        int k=WinEduca.lst.numeroest;
        for (int i=0;i<k;i++){
            linea=WinEduca.lst._lstest[i].getCedula()+", ";
            linea+=WinEduca.lst._lstest[i].getNombre();
            CmbLista.addItem(linea);
        }
    }//GEN-LAST:event_BtnListarActionPerformed

