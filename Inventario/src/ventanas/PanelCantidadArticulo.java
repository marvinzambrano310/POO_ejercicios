    private void verificarCantidad()
    { 
        int valor = Integer.parseInt(txtCantidad.getText());
        if(valor>=1)
        {   
            txtPrecioTotal.setText(String.valueOf((valor * art.getPrecioVenta())));
            fd.setPrecioVenta(Double.parseDouble(txtPrecioTotal.getText()));
        }        
    }
    private void verificarEntrada(ActionEvent ev)
    {
        int valor = Integer.parseInt(txtCantidad.getText());
        if(valor<1)
        {
            txtCantidad.setText("1");
           lbAviso.setVisible(true);
            return;
        }else
        {
            lbAviso.setVisible(false);
            cantidad = new Integer(txtCantidad.getText());
            fd.setPrecioVenta(art.getPrecioVenta());
            
            fd.setCantidad(cantidad); 
            opcion = 1;
            op.setValue(cantidad);
           
            
        }
    }
    
    private void cancelar(ActionEvent ev)
    {        
        op.setValue(2);
    }
    
    public JButton[] getButtons()
    {
        return new JButton[]{bntAceptar,bntCancelar};
    }
    
    private void getOptionPane()
    {
        if(op != null)
        {
            return;
        }
        Component pdr =this.getParent(); 
        while(pdr.getParent() != null)
        {
            if(pdr instanceof JOptionPane)
            {
                op = (JOptionPane)pdr;
                break;
            }            
            pdr = pdr.getParent();
        }
    }
    
