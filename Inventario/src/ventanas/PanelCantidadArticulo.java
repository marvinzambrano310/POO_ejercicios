    private void inicializar()
    {       
       
        txtNombreProducto.setText(art.getDescripcion());
        txtPrecioProducto.setText(art.getPrecioVenta().toString());
        txtPrecioTotal.setText(String.valueOf((cantidad * art.getPrecioVenta())));
        txtCantidad.setText(cantidad.toString());
        lbAviso.setVisible(false);
        
        bntAceptar.addActionListener(new ActionListener(){
          
            @Override
            public void actionPerformed(ActionEvent e) {
                getOptionPane();
                verificarEntrada(e);
            }
        });
        
        bntCancelar.addActionListener(new ActionListener(){

            @Override
            public void actionPerformed(ActionEvent e) {
                getOptionPane();
                cancelar(e);
            }
        });
        
        DocumentListener dcl = new DocumentListener(){

            @Override
            public void insertUpdate(DocumentEvent e) {                
                verificarCantidad();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                verificarCantidad();
            }
        };
        Helper.setFiltraEntrada(txtCantidad.getDocument(), FiltraEntrada.SOLO_NUMEROS, 7, false);
        txtCantidad.getDocument().addDocumentListener(dcl);
       
    }   

