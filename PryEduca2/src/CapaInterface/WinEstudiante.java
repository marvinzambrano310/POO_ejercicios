    private void BtnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCrearActionPerformed
        int k=WinEduca.lst.numeroest;
        cedula=TxtCedula.getText();
        nombre=TxtNombre.getText();
        boolean condicion=false;
        for(int i=0;i<k;i++){
            if (WinEduca.lst._lstest[i].getCedula().equals(cedula)){
                condicion=true;
            }
        }
        if (!condicion){
            WinEduca.lst._lstest[k]=new ClsEstudiante(cedula,nombre);
            LblMensaje.setText("Estudiante Registrado Correctamente");    
        }
        else{
            LblMensaje.setText("Estudiante Existente");
        }
        k+=1;
        WinEduca.lst.numeroest=k;
        TxtNombre.setText(null);
        TxtCedula.setText(null);
        TxtCedula.requestFocus();
    }//GEN-LAST:event_BtnCrearActionPerformed

    private void BtnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnListarActionPerformed
        LblMensaje.setText(null);
        String linea;
        
        linea=WinEduca.lst._lstest[k].getCedula()+", "+ WinEduca.lst._lstest[k].getNombre();
        modeloLista.addElement(linea);
        k++;
    }//GEN-LAST:event_BtnListarActionPerformed

