    private void BtnCoordYActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCoordYActionPerformed
        // TODO add your handling code here:
        y=Float.parseFloat(TxtDisplay.getText());
        if (primer){
            pi= new ClsPunto(x,y);
            TxtDisplay.setText("Punto Inicial: " + pi.rectangular());
            primer=false;
        }else{
            pf= new ClsPunto(x,y);
            primer=true;
            l1= new ClsLinea(pi,pf);
            TxtDisplay.setText(l1.VerLinea());
        }
        
        BtnCoordY.setEnabled(false);
    }//GEN-LAST:event_BtnCoordYActionPerformed

