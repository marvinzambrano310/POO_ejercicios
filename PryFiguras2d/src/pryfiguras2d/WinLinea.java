    private void BtnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnIgualActionPerformed
        // TODO add your handling code here:
        switch(codigo){
            case 1:{
                l1=l2.Suma(l1);
                break;
            }
            case 2:{
                l1=l2.Resta(l1);
                break;
            }
            case 3:{
                valor=Float.parseFloat(TxtDisplay.getText());
                l1=l2.Multiplicacion(valor);
                break;
            }
            case 4:{
                valor1=Float.parseFloat(TxtDisplay.getText());
                l1=l2.Division(valor);
                break;
            }
        }
        TxtDisplay.setText(l1.VerEcuacion());
    }//GEN-LAST:event_BtnIgualActionPerformed

