    public void limpiarTabla() {
        try {
            DefaultTableModel modelo = (DefaultTableModel) TblProveedores.getModel();
            int filas = TblProveedores.getRowCount();
            for (int i = 0; i <= filas; i++) {
                modelo.removeRow(0);
            }
        } catch (Exception e) {
            // JOptionPane.showMessageDialog(null, "Error al limpiar la tabla.");
        }
    }
    
    public void llenarTabla() {
        limpiarTabla();
        clte = new ClsConexion("C://Users//JONATHAN Y LUIS//Desktop//PryProyectoFinal//DBProyecto.accdb");
        //file.tablelistContactos(contactos, nombreArchivo);
        try {
            ResultSet tlf;
            tlf = clte.lista("TblProveedores");
            while (contactos.getRowCount() > 0) {
                contactos.removeRow(0);
            }
            list.llenarTabla(contactos, tlf);

        } catch (SQLException ex) {
            Logger.getLogger(WinRegistroClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
        TblProveedores.setModel(contactos);
    }
    
