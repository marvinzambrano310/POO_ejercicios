    private void BtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegistrarActionPerformed
        String nombre;
        String apellido;
        String cedula;
        String telefono;
        String tipo;
        String direccion;
        Date registro = null;

        nombre = TxtNombre.getText();
        apellido = TxtApellido.getText();
        cedula = TxtCedula.getText();
        telefono = TxtTelefono.getText();
        tipo = CmbTipo.getSelectedItem().toString();
        direccion = TxtDireccion.getText();

        list.agregar(registro, cedula, nombre, apellido, telefono, direccion, tipo);

        TxtNombre.setText("");
        TxtApellido.setText("");
        TxtCedula.setText("");
        TxtTelefono.setText("");
        TxtDireccion.setText("");
    }//GEN-LAST:event_BtnRegistrarActionPerformed

    private void BtnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarCambiosActionPerformed
        int Fila = TblClientes.getSelectedRow();
        String cedula = TblClientes.getValueAt(Fila, 0).toString();
        String nombre = TblClientes.getValueAt(Fila, 1).toString();
        String apellido = TblClientes.getValueAt(Fila, 2).toString();
        String tipo = TblClientes.getValueAt(Fila, 3).toString();
        String telefono = TblClientes.getValueAt(Fila, 4).toString();
        String direccion = TblClientes.getValueAt(Fila, 5).toString();
        //JOptionPane.showMessageDialog(null, nombre + " " + apellido + "- " + tipo + " - -" + telefono + "++" + direccion);
        Date registro = null;
        list.modificar(registro, cedula, nombre, apellido, telefono, direccion, tipo);
        llenarTabla();
    }//GEN-LAST:event_BtnGuardarCambiosActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        int Fila = TblClientes.getSelectedRow();
        String codigo = TblClientes.getValueAt(Fila, 0).toString();
        list.borrar(codigo);
        llenarTabla();
    }//GEN-LAST:event_BtnEliminarActionPerformed

